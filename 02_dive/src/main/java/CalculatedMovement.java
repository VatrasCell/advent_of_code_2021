public class CalculatedMovement {

    private int horizontal;
    private int depth;
    private int aim;

    public int getHorizontal() {
        return horizontal;
    }

    public void addHorizontal(int horizontal) {
        this.horizontal += horizontal;
    }

    public int getDepth() {
        return depth;
    }

    public void addDepth(int depth) {
        this.depth += depth;
    }

    public int getAim() {
        return aim;
    }

    public void addAim(int aim) {
        this.aim += aim;
    }
}
